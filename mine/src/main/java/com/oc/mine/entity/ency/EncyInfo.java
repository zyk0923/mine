package com.oc.mine.entity.ency;

public class EncyInfo {
	private Integer id;
	private String info_name;
	private String info_remark;
	private String info_conect;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getInfo_name() {
		return info_name;
	}
	public void setInfo_name(String info_name) {
		this.info_name = info_name;
	}
	public String getInfo_conect() {
		return info_conect;
	}
	public void setInfo_conect(String info_conect) {
		this.info_conect = info_conect;
	}
	public String getInfo_remark() {
		return info_remark;
	}
	public void setInfo_remark(String info_remark) {
		this.info_remark = info_remark;
	}
}
